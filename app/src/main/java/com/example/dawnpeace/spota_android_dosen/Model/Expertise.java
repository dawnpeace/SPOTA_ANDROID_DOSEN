package com.example.dawnpeace.spota_android_dosen.Model;

public class Expertise {
    private int id;
    private String name;


    public Expertise(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
